class Player {
  final String id;
  final String name;
  final String position;
  final String team;
  final String imageUrl;
  final int goals;
  final int assists;

  Player({
    required this.id,
    required this.name,
    required this.position,
    required this.team,
    required this.imageUrl,
    required this.goals,
    required this.assists,
  });
}
